int bqb_a2dp_test_command_handler(int argc, char *argv[])
{
	int ret = 0;
	char* cmd = argv[1];
	if (!strcmp(cmd, "start")) {
		bqb_a2dp_init();
	} else if (!strcmp(cmd, "stop")) {

	} else if (!strcmp(cmd, "conn")) {
		if (3 > argc) {
			ret = -1;
			BT_ERR("need input the role");
		} else {
			ret = bqb_a2dp_create_conn(argv[2], true);
		}
	} else if (!strcmp(cmd, "conn_acl")) {
		bqb_gap_clean_linkkey();
		if (3 > argc) {
			ret = -1;
			BT_ERR("need input the role");
		} else {
			ret = bqb_a2dp_create_conn(argv[2], false);
		}
	} else if (!strcmp(cmd, "disconn_acl")) {
		if (s_bqb_a2dp_ctx.conn) {
			ret = hostif_bt_conn_disconnect(s_bqb_a2dp_ctx.conn, BT_HCI_ERR_REMOTE_USER_TERM_CONN);
		} else {
			ret = -1;
		}
	} else if (!strcmp(cmd, "disconn_sig")) {
		hostif_bt_a2dp_disconnect(s_bqb_a2dp_ctx.conn);
	} else if (!strcmp(cmd, "regsrcep")) {
		bqb_a2dp_a2dp_register_src_endpoint();
	} else if (!strcmp(cmd, "regsnkep")) {
		bqb_a2dp_a2dp_register_sink_endpoint();
	} else if (!strcmp(cmd, "setcfg")) {
		ret = bt_a2dp_set_configuration(s_bqb_a2dp_ctx.conn);
	} else if (!strcmp(cmd, "open")) {
		ret = bqb_a2dp_test_open();
	} else if (!strcmp(cmd, "start")) {
		ret = hostif_bt_a2dp_start(s_bqb_a2dp_ctx.conn);
	} else if (!strcmp(cmd, "close")) {
		ret = bt_a2dp_close(s_bqb_a2dp_ctx.conn);
	} else if (!strcmp(cmd, "connect")) {
		ret = hostif_bt_a2dp_connect(s_bqb_a2dp_ctx.conn, BT_A2DP_CH_SOURCE);
	} else if (!strcmp(cmd, "getcap")) {
		ret = bt_a2dp_get_capabilities(s_bqb_a2dp_ctx.conn);
	} else if (!strcmp(cmd, "getallcap")) {
		ret = bt_a2dp_get_all_capabilities(s_bqb_a2dp_ctx.conn);
	} else if (!strcmp(cmd, "discover")) {
		ret = bt_a2dp_discover(s_bqb_a2dp_ctx.conn);
	} else if (!strcmp(cmd, "conn_audio")) {
		ret = hostif_bt_a2dp_connect(s_bqb_a2dp_ctx.conn, BT_A2DP_CH_MEDIA);
	}  else if (!strcmp(cmd, "disconn_audio")) {
		ret = hostif_bt_a2dp_disconnect(s_bqb_a2dp_ctx.conn);
	} else if (!strcmp(cmd, "senddata")) {
		for (uint8_t i = 0; i < 60; i++) {
			ret = hostif_bt_a2dp_send_audio_data(s_bqb_a2dp_ctx.conn, bqb_sbc_data44k, sizeof(bqb_sbc_data44k));
			if (ret < 0) {
				SYS_LOG_ERR("send data error %d\n", ret);
			}
			k_sleep(10);
		}
	} else if (!strcmp(cmd, "senddata48")){
		for (uint8_t i = 0; i < 60; i++) {
			ret = hostif_bt_a2dp_send_audio_data(s_bqb_a2dp_ctx.conn, bqb_sbc_data48k, sizeof(bqb_sbc_data48k));
			if (ret < 0) {
				SYS_LOG_ERR("send data error %d\n", ret);
			}
			k_sleep(10);
		}
	} else if (!strcmp(cmd, "suspend")) {
		ret = hostif_bt_a2dp_suspend(s_bqb_a2dp_ctx.conn);
	} else if (!strcmp(cmd, "recfg")) {
		struct bt_a2dp_media_codec* codec = bt_a2dp_get_seted_codec(s_bqb_a2dp_ctx.conn);
		ret = bt_a2dp_reconfig(s_bqb_a2dp_ctx.conn, codec);
	} else if (!strcmp(cmd, "abort")) {
		ret = bt_a2dp_abort(s_bqb_a2dp_ctx.conn);
	}
	return 0;
}

