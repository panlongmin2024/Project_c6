typedef struct
{
	struct bt_l2cap_chan_ops ops;
	struct bt_l2cap_br_chan br_chan;
	struct bt_conn* conn;
	bool auto_conn_a2dp;
	uint8_t role;
}bqb_a2dp_context_t;

static bqb_a2dp_context_t s_bqb_a2dp_ctx;

static void bqb_a2dp_acl_connected(struct bt_conn *conn, u8_t err)
{
	BT_INFO("type: %d, error: 0x%x",hostif_bt_conn_get_type(conn), err);

	if (err) {
		//hostif_bt_conn_unref(s_bqb_a2dp_ctx.conn);
		s_bqb_a2dp_ctx.conn = NULL;
	} else {
		s_bqb_a2dp_ctx.conn = conn;

		if (s_bqb_a2dp_ctx.auto_conn_a2dp) {
			hostif_bt_a2dp_connect(conn, s_bqb_a2dp_ctx.role);
		}
	}
}

static bool bqb_a2dp_acl_connect_req(bt_addr_t *peer,uint8_t *cod)
{
	BT_INFO("cod: 0x%x", *cod);
	return true;
}

static void bqb_a2dp_acl_disconnected(struct bt_conn *conn, uint8_t reason)
{
	BT_INFO("disconnected : 0x%x", reason);
	if (conn == s_bqb_a2dp_ctx.conn) {
		s_bqb_a2dp_ctx.conn = NULL;
		s_bqb_a2dp_ctx.auto_conn_a2dp = false;
	}
}

static struct bt_conn_cb s_bqb_a2dp_bt_conn_cb = {
	.connect_req = bqb_a2dp_acl_connect_req,
	.connected = bqb_a2dp_acl_connected,
	.disconnected = bqb_a2dp_acl_disconnected,
};

static int bqb_a2dp_create_conn(char* role, bool conn_a2dp)
{
	bt_addr_t addr = {0};
	if (!strcmp(role, "src")) {
		s_bqb_a2dp_ctx.role = BT_A2DP_CH_SOURCE;
	} else if (!strcmp(role, "snk")) {
		s_bqb_a2dp_ctx.role = BT_A2DP_CH_SINK;
	} else {
		BT_ERR("role is error");
		return -1;
	}
	if (s_bqb_a2dp_ctx.conn) {
		hostif_bt_a2dp_connect(s_bqb_a2dp_ctx.conn, s_bqb_a2dp_ctx.role);
	} else {
		s_bqb_a2dp_ctx.auto_conn_a2dp = conn_a2dp;

		bqb_utils_read_pts_addr(&addr);
		s_bqb_a2dp_ctx.conn = hostif_bt_conn_create_br(&addr, BT_BR_CONN_PARAM_DEFAULT);
		if (!s_bqb_a2dp_ctx.conn) {
			BT_ERR("create acl failed");
			return -1;
		}
		hostif_bt_conn_unref(s_bqb_a2dp_ctx.conn);
	}
	return 0;
}

